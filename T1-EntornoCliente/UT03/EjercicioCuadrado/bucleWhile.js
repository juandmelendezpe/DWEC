//while es el bucle más simple de JavaScript. Se ejecuta un bloque de código mientras se cumpla una condición específica.
//La sintaxis es la siguiente:
while (condición) {
//  // código a ejecutar
}
//En este caso, el bucle se ejecutará mientras la variable x sea menor que 10.
function bucleWhile() {
    var x = 0;
    while (x < 10) {
        x++;
    }
    console.log(x);
    }

//Cuando x sea igual a 10, el bucle se detendrá.
//El bucle se ejecutará 10 veces, ya que x se incrementa en 1 en cada iteración.
